use store
CREATE TABLE Categories(id INT IDENTITY(1,1) PRIMARY KEY,
					  name VARCHAR(100) NOT NULL)

CREATE TABLE Countries(id INT IDENTITY(1,1) PRIMARY KEY,
					  name VARCHAR(100) NOT NULL)

CREATE TABLE Measurements(id INT IDENTITY(1,1) PRIMARY KEY,
					  name VARCHAR(100) NOT NULL,
					  _description VARCHAR(255))

CREATE TABLE Markups(id INT IDENTITY(1,1) PRIMARY KEY,
					  name VARCHAR(100) NOT NULL,
					  _percent INT NOT NULL)

CREATE TABLE Addresses(id INT IDENTITY(1,1) PRIMARY KEY,
					  idCountry INT NOT NULL FOREIGN KEY REFERENCES Countries(id),
					  town VARCHAR(100) NOT NULL,
					  street VARCHAR(100) NOT NULL)

CREATE TABLE Producers(id INT IDENTITY(1,1) PRIMARY KEY,
					  name VARCHAR(100) NOT NULL,
					  idAddress INT NOT NULL FOREIGN KEY REFERENCES Addresses(id))

CREATE TABLE Products(id int identity(1,1) primary key,
					  name VARCHAR(100) NOT NULL,
					  price FLOAT NOT NULL,
					  quantity FLOAT NOT NULL,
					  idMeasurement INT FOREIGN KEY REFERENCES Measurements(id),
					  idCategory INT NOT NULL FOREIGN KEY REFERENCES Categories(id),
					  idProducer INT FOREIGN KEY REFERENCES Producers(id),
					  idMarkup INT FOREIGN KEY REFERENCES Markups(id))

CREATE TABLE Sales(id INT IDENTITY(1,1) PRIMARY KEY,
					  idProduct INT NOT NULL FOREIGN KEY REFERENCES Products(id),
					  price FLOAT NOT NULL,
					  quantity INT NOT NULL,
					  dateSale SMALLDATETIME NOT NULL)

CREATE TABLE Suppliers(id INT IDENTITY(1,1) PRIMARY KEY,
					  name VARCHAR(100) NOT NULL,
					  idAddress INT NOT NULL FOREIGN KEY REFERENCES Addresses(id))

CREATE TABLE Deliveries(id INT IDENTITY(1,1) PRIMARY KEY,
					  idProduct INT NOT NULL FOREIGN KEY REFERENCES Products(id),
					  idSupplier INT NOT NULL FOREIGN KEY REFERENCES Suppliers(id),
					  quantity INT NOT NULL,
					  price FLOAT NOT NULL,
					  dateDelivery SMALLDATETIME NOT NULL)


